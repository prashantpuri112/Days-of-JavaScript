/* Find Method: So as the name says, we can use the Find method to retrieve one element  of an array based on a condition.
 Find is basically just another method that loops over the array but then it does something different. The Find method is a bit similar to the Filter method,
but there are two fundamental differences.

    First Filter returns all the elements that match the condition while the Find method only returns the first one
    Second and even more important, the Filter method returns a new array while Find only returns the element itselfand not an array
*/

